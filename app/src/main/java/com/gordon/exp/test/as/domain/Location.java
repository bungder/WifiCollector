package com.gordon.exp.test.as.domain;

/**
 * Place coordination
 * @author Gordon
 * @date 2015-8-2
 *
 */
public class Location {

	private int x;
	private int y;
	public Location(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	
}
